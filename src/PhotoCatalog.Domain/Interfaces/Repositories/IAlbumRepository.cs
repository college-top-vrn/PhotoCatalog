using PhotoCatalog.Domain.Entities;
using PhotoCatalog.Domain.Primitives;

namespace PhotoCatalog.Domain.Interfaces.Repositories;

/// <summary>
/// Предоставляет методы для управления виртуальными альбомами в репозитории.
/// </summary>
/// <remarks>
/// Этот интерфейс определяет контракт для операций CRUD (создание, чтение, обновление, удаление)
/// с объектами типа <see cref="Album"/>. Все методы возвращают результат, обернутый в 
/// <see cref="Result"/> или <see cref="Result{T}"/>, что позволяет единообразно обрабатывать 
/// успешные и ошибочные сценарии.
/// </remarks>
public interface IAlbumRepository
{
    /// <summary>
    /// Получает альбом по его уникальному идентификатору.
    /// </summary>
    /// <param name="id">Уникальный идентификатор альбома.</param>
    /// <returns>
    /// Объект <see cref="Result{T}"/>, содержащий найденный альбом в случае успеха,
    /// или информацию об ошибке, если альбом не найден или произошла ошибка доступа к данным.
    /// </returns>
    /// <remarks>
    /// Если альбом с указанным идентификатором не существует, возвращается 
    /// неуспешный Result с соответствующим сообщением об ошибке.
    /// </remarks>
    Result<Album> GetById(int id);

    /// <summary>
    /// Добавляет новый альбом в репозиторий.
    /// </summary>
    /// <param name="album">Объект альбома для добавления. Не может быть null.</param>
    /// <returns>
    /// Объект <see cref="ResultVoid"/>, указывающий на успешность выполнения операции.
    /// Возвращает успешный результат, если альбом успешно добавлен,
    /// или неуспешный с описанием ошибки в противном случае.
    /// </returns>
    /// <remarks>
    /// Перед добавлением может выполняться проверка уникальности имени альбома 
    /// или других бизнес-правил. В случае нарушения этих правил возвращается ошибка.
    /// </remarks>
    ResultVoid Add(Album album);

    /// <summary>
    /// Обновляет существующий альбом в репозитории.
    /// </summary>
    /// <param name="album">Объект альбома с обновленными данными. Не может быть null.</param>
    /// <returns>
    /// Объект <see cref="ResultVoid"/>, указывающий на успешность выполнения операции.
    /// Возвращает успешный результат, если альбом успешно обновлен,
    /// или неуспешный с описанием ошибки (например, если альбом не найден).
    /// </returns>
    /// <remarks>
    /// Обновление выполняется только для существующего альбома. Если альбом с 
    /// указанным идентификатором не найден в репозитории, возвращается ошибка.
    /// </remarks>
    ResultVoid Update(Album album);

    /// <summary>
    /// Удаляет альбом из репозитория по его идентификатору.
    /// </summary>
    /// <param name="id">Уникальный идентификатор альбома для удаления.</param>
    /// <returns>
    /// Объект <see cref="ResultVoid"/>, указывающий на успешность выполнения операции.
    /// Возвращает успешный результат, если альбом успешно удален,
    /// или неуспешный с описанием ошибки (например, если альбом не найден).
    /// </returns>
    /// <remarks>
    /// При удалении альбома может потребоваться также удалить или обновить связанные 
    /// данные (например, фотографии или ссылки на этот альбом). Рекомендуется 
    /// реализовать каскадное удаление или соответствующую бизнес-логику.
    /// </remarks>
    ResultVoid Delete(int id);
}